using System;

namespace monolithsvc.Models
{

    public class sharedMath
    {
        public int addTwoNumbers (int a, int b)
        {
            return a+b;
        }

        public int multiplyTwoNumbers(int a, int b)
        {
            return a*b;
        }

        public int minusTwoNumbers(int a, int b)
        {
            return a-b;
        }
    }


}